// SWITCHES- A way to valid conditions
// recommended in case there are many cases 

const color = 'yellow';

switch(color){
  case 'red':
    console.log('Color is red');
    break;
  case 'blue':
    console.log('Color is blue');
    break;
  default:
    console.log('Color is not red or blue');
    break;
}

let day;

// To add one to current date
